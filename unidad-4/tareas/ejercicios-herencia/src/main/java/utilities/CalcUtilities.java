package utilities;

/**
 * CalcUtilities class used for calculations
 * @author Jose Maximiliano Boada Martin
 */
public class CalcUtilities {
    public float add(float valueA, float valueB) {
        return valueA + valueB;
    }

    public float subtract(float valueA, float valueB) {
        return valueA - valueB;
    }

    public float multiply(float valueA, float valueB) {
        return valueA * valueB;
    }

    public float divide(float valueA, float valueB) {
        return valueA / valueB;
    }
}
